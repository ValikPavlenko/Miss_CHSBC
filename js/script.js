$(document).ready(function(){
    var touch = $('#touchMenu'),
    menu = $('#menu'), line_b=$('.line-b'),
    h_t=$(".item-participants").position().top,// позиція верха
    h_b=$('#competition').position().top;
    $(".menu-s").on("click","a", function (event) {
        event.preventDefault();
        var id  = $(this).attr('href'),
            top = $(id).offset().top ;
        $('#line-2').removeClass("close");
        line_b.removeClass("cmn-toggle-switch_3 cmn-toggle-switch_1 cmn-toggle-switch_close line-b-close");
        $('body,html').animate({scrollTop: (top - 40) }, 1500);
    });
    //read competition
    $('#read-competition').on('click', function () {
        $("#close_t").toggleClass('close');
        $('#continuation').toggleClass('close');
        $('#arrow').toggleClass('arrow-r');
        $('#read').toggleClass('close');
        $('body,html').animate({scrollTop: $("#competition").offset().top - 100}, 500);
    });
    $(touch).on('click', function(e){
        if( !$('#line-2').hasClass('close')){
            line_b.addClass("cmn-toggle-switch_close");
            line_b.addClass('line-b-close');
            $('#line-2').addClass('close');
            $('#line-3').addClass("cmn-toggle-switch_3");
            $('#line-1').addClass("cmn-toggle-switch_1");
        }
        else {
            $('#line-2').removeClass("close");
            line_b.removeClass("cmn-toggle-switch_3 cmn-toggle-switch_1 cmn-toggle-switch_close line-b-close")
        }
        e.preventDefault();
        menu.slideToggle();
    });
    $("#menu a").on('click', function(e) {
        var wid = $(window).width();
        if(wid < 800 ) {
            e.preventDefault();
            menu.slideToggle();
        }
    });
    $(window).scroll(function(){
        var winScrollTop = $(this).scrollTop();
        if(winScrollTop < h_t + 50){
            (function (){
                windo.css("top", h_t);
            })();
        }
        if((winScrollTop+w_h) >(h_b-20)){
            (function (){
                windo.css("top", (h_b-w_h));
            })();
        }
        if((winScrollTop+hed_h+50)>h_t && (winScrollTop + 50) <h_b-w_h-50){
            (function (){
                windo.css("top", (winScrollTop+hed_h+50));
            })();
        }
    });
});
