var r=0,w_h=0, hed_h=$('header').height(), sl = $('#start'), windo=$('#windows');
function start(n) {
    $('#sl-img').attr("src","img/"+n+".jpg");
    r=n;
    n-=1;
    $('#name').text(Name[n]+",");
    $('#facultat').text('студентка групи '+faculty[n]);
    $('#text').text(text[n]);
    w_h=$('#windows').height();
}
function windows(n) {
    windo.removeClass('close');
    start(n);
    // $('body,html').animate({scrollTop: (windo.offset().top - 100)}, 500);
}
function exit() {
    windo.addClass('close')
}
function Next() {
    sl.addClass('left-w opacity_0');
    setTimeout(function(){
        sl.removeClass('left-w');
        sl.addClass('right-w');

        if(r!=12) {
            start(r + 1);
        }
        else {
            start(1)
        }
    },350);
    setTimeout(function () {
        sl.removeClass('opacity_0 right-w')
    },800)
}
function left() {
    sl.addClass('right-w opacity_0');
    setTimeout(function(){
        sl.removeClass('right-w');
        sl.addClass('left-w');
        if(r!=1) {
            start(r-1);
        }
        else {
            start(12)
        }
    },350);
    setTimeout(function () {
        sl.removeClass('opacity_0 left-w')
    },800)
}
